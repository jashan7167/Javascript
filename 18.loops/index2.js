//for loops - repeat code a specific number of times

for (let i = 0; i <=10; i++) {
  console.log(i);
}

for (let i = 0; i <=10; i+=2) {
  console.log(i);
}



for (let i = 10; i >0; i--) {

  if(i===5)
  {
    continue;
  }
  console.log(i);
}
for (let i = 10; i >0; i--) {

  if(i===5)
  {
    break;
  }
  console.log(i);
}




